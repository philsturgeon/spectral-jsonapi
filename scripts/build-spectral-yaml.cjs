#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");
const { stringify } = require("yaml");
const spectralFunctions = require("@stoplight/spectral-functions");
const spectralFormats = require("@stoplight/spectral-formats");

const repoRoot = path.resolve(__dirname, "..");
const distRulesetPath = path.join(repoRoot, "dist", "ruleset.js");
const outputPath = path.join(repoRoot, ".spectral.yml");

if (!fs.existsSync(distRulesetPath)) {
  console.error("Missing dist/ruleset.js. Run `npm run build` first.");
  process.exit(1);
}

const loaded = require(distRulesetPath);
const ruleset = loaded.default || loaded;

if (!ruleset || typeof ruleset !== "object") {
  console.error("The compiled ruleset export is not an object.");
  process.exit(1);
}

const functionNameByRef = new Map(
  Object.entries(spectralFunctions)
    .filter(([, value]) => typeof value === "function")
    .map(([name, fn]) => [fn, name]),
);

const functionNames = new Set(Object.keys(spectralFunctions));

const formatNameByRef = new Map(
  Object.entries(spectralFormats).map(([name, value]) => [value, name]),
);

const formatNameByMeta = new Map(
  Object.entries(spectralFormats)
    .filter(([, value]) => typeof value === "function")
    .flatMap(([name, value]) => {
      const candidates = [value.name, value.displayName].filter(Boolean);
      return candidates.map((candidate) => [candidate, name]);
    }),
);

function normalize(value, atPath = "$") {
  if (Array.isArray(value)) {
    return value.map((nested, index) =>
      normalize(nested, `${atPath}[${index}]`),
    );
  }

  if (typeof value === "function") {
    const functionName = functionNameByRef.get(value);
    if (functionName) {
      return functionName;
    }

    if (functionNames.has(value.name)) {
      return value.name;
    }

    const formatName =
      formatNameByMeta.get(value.name) ||
      formatNameByMeta.get(value.displayName);
    if (formatName) {
      return formatName;
    }

    throw new Error(
      `Encountered an unmapped function at ${atPath}: ${value.name || "<anonymous>"}`,
    );
  }

  if (value && typeof value === "object") {
    const formatName = formatNameByRef.get(value);
    if (formatName) {
      return formatName;
    }

    const out = {};
    for (const [key, nested] of Object.entries(value)) {
      out[key] = normalize(nested, `${atPath}.${key}`);
    }
    return out;
  }

  return value;
}

const normalizedRuleset = normalize(ruleset);
const yaml = stringify(normalizedRuleset, {
  lineWidth: 0,
  blockQuote: "literal",
  defaultStringType: "PLAIN",
});

const generatedHeader = [
  "# This file is generated by scripts/build-spectral-yaml.cjs",
  "# Do not edit this file directly; edit src/ruleset.ts and rebuild.",
  "",
].join("\n");

fs.writeFileSync(outputPath, generatedHeader + yaml, "utf8");
console.log(
  `Generated ${path.relative(repoRoot, outputPath)} from dist/ruleset.js`,
);
